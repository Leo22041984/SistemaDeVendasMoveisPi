    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            // Solicita o idFuncionario ao usu√°rio
            String idFuncionarioStr = JOptionPane.showInputDialog(null, "Digite o ID do funcion√°rio a ser exclu√≠do:");

            // Verifica se o usu√°rio cancelou a opera√ß√£o
            if (idFuncionarioStr == null) {
                return;
            }

            // Valida se o idFuncionario √© um n√∫mero v√°lido
            if (!idFuncionarioStr.matches("\\d+")) {
                JOptionPane.showMessageDialog(null, "Por favor, insira um n√∫mero v√°lido para o ID do funcion√°rio.");
                return;
            }

            int idFuncionario = Integer.parseInt(idFuncionarioStr);

            // Chama o m√©todo para excluir o funcion√°rio
            Funcionario funcionario = new Funcionario();
            boolean excluido = funcionario.excluirFuncionario(idFuncionario);
            if (excluido) {
                // Limpa os campos ap√≥s a exclus√£o
                limparCampos();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao converter o ID do funcion√°rio para n√∫mero: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

